function add(num1, num2) {
    return num1 + num2;
}
function subtract(num1, num2) {
    return num1 - num2;
}
function divide(num1, num2) {
    return num1 / num2;
}
function multiply(num1, num2) {
    return num1 * num2;
}



console.log(add(5,7));
console.log(subtract(8,3));
console.log(divide(16,4));
console.log(multiply(10,6));